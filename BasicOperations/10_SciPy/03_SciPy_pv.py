import scipy as sp
'''
每期利息率为：0.05
期数：3
每月等额收支：0
终值：100
求：现值
'''
pv1 = sp.pv(0.05,3,0,100,when='begin')
print(pv1)

'''
每期利息率为：0.05/12
期数：10*12
每月等额收支：+100
终值：0
求：现值
'''
pv2 = sp.pv(0.05/12, 10*12, 100, 0,when = 'begin')
print(pv2)
'''
每期利息率为：0.05/12
期数：30*12
每月等额收支：+3000
终值：0
求：现值
60岁开始，每月领取3000元，共领取30年领完，60随时需要存入多少钱
'''
pv3 = sp.pv(0.05/12,30*12,3000,0,when = 'begin')
print(pv3)